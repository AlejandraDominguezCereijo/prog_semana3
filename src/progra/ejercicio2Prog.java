public class ejercicio2Prog {
    public static void main(String[] args) {
        final double PI = 3.1415;
        int r = 3;
        double area = PI * (r * r);
        double longitud = 2 * PI * r;
        System.out.println("El area de la circunferencia es "+area+", la longitud de la circunferencia es "+longitud);
    }

}
